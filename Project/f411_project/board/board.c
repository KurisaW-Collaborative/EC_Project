/*
 *TIM5定时器实现us级延时
 */
void Delay_us(uint16_t delay)
{
    __HAL_TIM_DISABLE(&htim5);
    __HAL_TIM_SET_COUNTER(&htim5,0);
    __HAL_TIM_ENABLE(&htim5);
    uint16_t curCnt=0;
    while(1)
    {
        curCnt=__HAL_TIM_GET_COUNTER(&htim5);
        if(curCnt>=delay)
            break;
    }
    __HAL_TIM_DISABLE(&htim5);
}

